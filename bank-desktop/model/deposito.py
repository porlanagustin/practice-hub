        self.valorDocumento = valorDocumento
        self.ciudad = ciudad
        self.sexo = sexo
        self.cbuNumber = cbuNumber
        self.email = email
        self.motivo = motivo
        self.tipoMoneda = tipoMoneda
        self.monto = monto